CREATE OR REPLACE PACKAGE BODY PK_CREDITO AS
    PROCEDURE ANALISAR_CAPACIDADE(
        P_COD_CLI IN NUMBER,
        P_VALOR_SOLICITADO IN NUMBER,
        P_QTD_PARCELAS IN NUMBER,
        P_RESULTADO_NEGOCIO OUT VARCHAR2,
        P_MENSAGEM OUT VARCHAR2,
        P_PAYLOAD_JSON OUT CLOB
    ) IS
        V_RENDA NUMBER;
        V_PARCELA_MENSAL NUMBER;
        V_COMPROMETIMENTO NUMBER;
    BEGIN
        SELECT NVL(RENDA_COMPROVADA, 0) INTO V_RENDA
        FROM CLIENTES WHERE COD_CLI = P_COD_CLI;

        V_PARCELA_MENSAL := P_VALOR_SOLICITADO / P_QTD_PARCELAS;
        V_COMPROMETIMENTO := (V_PARCELA_MENSAL / NULLIF(V_RENDA, 0)) * 100;

        IF V_COMPROMETIMENTO <= 30 THEN
            P_RESULTADO_NEGOCIO := 'APROVADO';
            P_MENSAGEM := 'Capacidade de pagamento adequada';
        ELSIF V_COMPROMETIMENTO <= 50 THEN
            P_RESULTADO_NEGOCIO := 'ALERTA';
            P_MENSAGEM := 'Comprometimento de renda elevado';
        ELSE
            P_RESULTADO_NEGOCIO := 'REPROVADO';
            P_MENSAGEM := 'Comprometimento de renda acima do limite';
        END IF;

        P_PAYLOAD_JSON := '{"renda":' || V_RENDA ||
                          ',"parcelaMensal":' || V_PARCELA_MENSAL ||
                          ',"comprometimento":' || ROUND(V_COMPROMETIMENTO, 2) || '}';

        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                P_RESULTADO_NEGOCIO := 'REPROVADO';
                P_MENSAGEM := 'Cliente nao encontrado';
                P_PAYLOAD_JSON := '{"erro":"Cliente nao encontrado"}';
            WHEN OTHERS THEN
                P_RESULTADO_NEGOCIO := 'INCONCLUSIVO';
                P_MENSAGEM := 'Erro ao analisar capacidade: ' || SQLERRM;
                P_PAYLOAD_JSON := '{"erro":"' || REPLACE(SQLERRM, '"', '''') || '"}';
    END ANALISAR_CAPACIDADE;
END PK_CREDITO;
