            P_RESULTADO_NEGOCIO := 'APROVADO';
            P_MENSAGEM := 'SCR sem restricoes impeditivas';
        ELSE
            P_RESULTADO_NEGOCIO := 'ALERTA';
            P_MENSAGEM := 'Valor total em risco elevado';
        END IF;

        P_PAYLOAD_JSON := '{"operacoesAtivas":' || V_OPERACOES_ATIVAS ||
                          ',"valorTotalRisco":' || V_VALOR_TOTAL_RISCO || '}';

        EXCEPTION
            WHEN OTHERS THEN
                P_RESULTADO_NEGOCIO := 'INCONCLUSIVO';
                P_MENSAGEM := 'Erro ao consultar SCR: ' || SQLERRM;
                P_PAYLOAD_JSON := '{"erro":"' || REPLACE(SQLERRM, '"', '''') || '"}';
    END CONSULTAR_SCR;
END PK_BACEN;
