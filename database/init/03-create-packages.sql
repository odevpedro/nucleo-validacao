        P_PAYLOAD_JSON OUT CLOB
    ) IS
        V_PERFIL VARCHAR2(30);
    BEGIN
        SELECT NVL(PERFIL_INVESTIDOR, 'NAO_INFORMADO') INTO V_PERFIL
        FROM CLIENTES WHERE COD_CLI = P_COD_CLI;

        IF V_PERFIL != 'NAO_INFORMADO' THEN
            P_RESULTADO_NEGOCIO := 'APROVADO';
            P_MENSAGEM := 'Perfil de investidor valido';
        ELSE
            P_RESULTADO_NEGOCIO := 'ALERTA';
            P_MENSAGEM := 'Perfil de investidor nao informado';
        END IF;

        P_PAYLOAD_JSON := '{"perfilInvestidor":"' || V_PERFIL || '"}';

        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                P_RESULTADO_NEGOCIO := 'REPROVADO';
                P_MENSAGEM := 'Cliente nao encontrado';
                P_PAYLOAD_JSON := '{"erro":"Cliente nao encontrado"}';
    END VALIDAR_PERFIL_INVESTIDOR;

