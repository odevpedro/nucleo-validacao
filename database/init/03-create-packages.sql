CREATE OR REPLACE PACKAGE BODY PK_SCORE AS
    PROCEDURE CALCULAR_SCORE_INTERNO(
        P_COD_CLI IN NUMBER,
        P_RESULTADO_NEGOCIO OUT VARCHAR2,
        P_MENSAGEM OUT VARCHAR2,
        P_PAYLOAD_JSON OUT CLOB
    ) IS
        V_SCORE NUMBER;
        V_SCORE_MINIMO CONSTANT NUMBER := 600;
    BEGIN
        SELECT NVL(SCORE_INTERNO, 0) INTO V_SCORE
        FROM CLIENTES WHERE COD_CLI = P_COD_CLI;

        IF V_SCORE >= V_SCORE_MINIMO THEN
            P_RESULTADO_NEGOCIO := 'APROVADO';
            P_MENSAGEM := 'Score interno aprovado';
        ELSE
            P_RESULTADO_NEGOCIO := 'REPROVADO';
            P_MENSAGEM := 'Score interno abaixo do minimo';
        END IF;

        P_PAYLOAD_JSON := '{"score":' || V_SCORE || ',"scoreMinimo":' || V_SCORE_MINIMO || '}';

        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                P_RESULTADO_NEGOCIO := 'REPROVADO';
                P_MENSAGEM := 'Cliente nao encontrado';
                P_PAYLOAD_JSON := '{"erro":"Cliente nao encontrado"}';
            WHEN OTHERS THEN
                P_RESULTADO_NEGOCIO := 'INCONCLUSIVO';
                P_MENSAGEM := 'Erro ao calcular score: ' || SQLERRM;
                P_PAYLOAD_JSON := '{"erro":"' || REPLACE(SQLERRM, '"', '''') || '"}';
    END CALCULAR_SCORE_INTERNO;
END PK_SCORE;
