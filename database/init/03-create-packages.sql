        P_COD_CLI IN NUMBER,
        P_RESULTADO_NEGOCIO OUT VARCHAR2,
        P_MENSAGEM OUT VARCHAR2,
        P_PAYLOAD_JSON OUT CLOB
    ) IS
        V_OPERACOES_ATIVAS NUMBER := 0;
        V_VALOR_TOTAL_RISCO NUMBER := 0;
    BEGIN
        SELECT COUNT(*), NVL(SUM(VALOR_TOTAL), 0)
        INTO V_OPERACOES_ATIVAS, V_VALOR_TOTAL_RISCO
        FROM CONTRATOS
        WHERE COD_CLI = P_COD_CLI AND STATUS IN ('ATIVO', 'VENCIDO');

