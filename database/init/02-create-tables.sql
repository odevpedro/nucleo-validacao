ALTER SESSION SET CONTAINER = FREEPDB1;
ALTER SESSION SET CURRENT_SCHEMA = BANK_CORE;

CREATE TABLE CLIENTES (
    COD_CLI NUMBER PRIMARY KEY,
    CPF_CNPJ VARCHAR2(20) NOT NULL,
    NOME VARCHAR2(150) NOT NULL,
    SCORE_INTERNO NUMBER,
    STATUS_CADASTRAL VARCHAR2(30),
    RENDA_COMPROVADA NUMBER(15,2),
    PERFIL_INVESTIDOR VARCHAR2(30),
    DATA_CADASTRO DATE DEFAULT SYSDATE
);

CREATE TABLE CONTAS (
    AGENCIA VARCHAR2(10) NOT NULL,
    CONTA VARCHAR2(20) NOT NULL,
    COD_CLI NUMBER NOT NULL,
    SALDO_DISPONIVEL NUMBER(15,2),
    STATUS VARCHAR2(30),
    LIMITE_PIX_DIARIO NUMBER(15,2),
    PRIMARY KEY (AGENCIA, CONTA)
);

CREATE TABLE CONTRATOS (
    CONTRATO_ID NUMBER PRIMARY KEY,
    COD_CLI NUMBER NOT NULL,
    TIPO_OPERACAO VARCHAR2(50),
    STATUS VARCHAR2(30),
    VALOR_TOTAL NUMBER(15,2),
    QTD_PARCELAS NUMBER,
    DIAS_ATRASO NUMBER
);

CREATE TABLE CHAVES_PIX (
    CHAVE VARCHAR2(150) PRIMARY KEY,
    TIPO VARCHAR2(30),
    COD_CLI NUMBER NOT NULL,
    STATUS VARCHAR2(30)
);

CREATE TABLE PROPOSTAS_CREDITO (
    PROPOSTA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COD_CLI NUMBER NOT NULL,
    VALOR_SOLICITADO NUMBER(15,2),
    QTD_PARCELAS NUMBER,
    TAXA_MENSAL NUMBER(10,4),
    CET NUMBER(10,4),
    STATUS VARCHAR2(30),
    DATA_CRIACAO TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE OPERACOES_PIX (
    ID_OPERACAO VARCHAR2(100) PRIMARY KEY,
    AGENCIA_ORIGEM VARCHAR2(10),
    CONTA_ORIGEM VARCHAR2(20),
    CHAVE_DESTINO VARCHAR2(150),
    VALOR NUMBER(15,2),
    STATUS VARCHAR2(30),
    DATA_CRIACAO TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE BOLETOS (
    NOSSO_NUMERO VARCHAR2(50) PRIMARY KEY,
    CPF_CNPJ_PAGADOR VARCHAR2(20),
    VALOR NUMBER(15,2),
    DATA_VENCIMENTO DATE,
    STATUS VARCHAR2(30)
);

CREATE TABLE RASTRO_GRUPO (
    ID_GRUPO_SOLICITACAO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_GRUPO_DEFINICAO NUMBER NOT NULL,
    NOME_GRUPO VARCHAR2(150) NOT NULL,
    ESTADO VARCHAR2(40) NOT NULL,
    PARAMETROS_ENTRADA CLOB,
    DATA_INICIO TIMESTAMP DEFAULT SYSTIMESTAMP,
    DATA_FIM TIMESTAMP,
    RESULTADO_FINAL_JSON CLOB,
    USUARIO_DB VARCHAR2(100),
    CORRELATION_ID VARCHAR2(100)
);

CREATE TABLE RASTRO_CONSISTENCIA (
    ID_LOG NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_GRUPO_SOLICITACAO NUMBER NOT NULL,
    ID_CONSISTENCIA_DEF NUMBER NOT NULL,
    NOME_CONSISTENCIA VARCHAR2(150) NOT NULL,
    PROCEDURE_REF VARCHAR2(200) NOT NULL,
    ESTADO_TECNICO VARCHAR2(40) NOT NULL,
    RESULTADO_NEGOCIO VARCHAR2(40),
    TEMPO_MS NUMBER,
    PAYLOAD_RETORNO CLOB,
    SQL_STATE VARCHAR2(20),
    SQL_ERROR_CODE NUMBER,
    MENSAGEM_ERRO VARCHAR2(4000),
    USUARIO_DB VARCHAR2(100),
    DATA_EXECUCAO TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_RASTRO_CONSISTENCIA_GRUPO
        FOREIGN KEY (ID_GRUPO_SOLICITACAO)
        REFERENCES RASTRO_GRUPO(ID_GRUPO_SOLICITACAO)
        ON DELETE CASCADE
);

CREATE INDEX IDX_RASTRO_GRUPO_ESTADO ON RASTRO_GRUPO(ESTADO);
CREATE INDEX IDX_RASTRO_GRUPO_DATA ON RASTRO_GRUPO(DATA_INICIO);
CREATE INDEX IDX_RASTRO_CONSISTENCIA_GRUPO ON RASTRO_CONSISTENCIA(ID_GRUPO_SOLICITACAO);
CREATE INDEX IDX_RASTRO_CONSISTENCIA_ESTADO ON RASTRO_CONSISTENCIA(ESTADO_TECNICO);
